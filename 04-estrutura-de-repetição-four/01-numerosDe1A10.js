// Escreva uma função chamada printNumbers que imprime todos os números de 1 a 10 usando um loop for.

function printNumbers() { 
  for (let i = 1; i <= 10; i++){    // vai continuar enquanto o i for menor que 10     ++ === incremento
    console.log(i)
  }
}

printNumbers()


